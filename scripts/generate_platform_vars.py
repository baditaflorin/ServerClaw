def resolve_dns_target(target: str, host_vars: dict[str, Any]) -> str:
    if target == MANAGEMENT_IPV4_TOKEN:
        return require_string(host_vars.get("management_ipv4"), "host_vars.management_ipv4")
    if target == MANAGEMENT_TAILSCALE_IPV4_TOKEN:
        return require_string(host_vars.get("management_tailscale_ipv4"), "host_vars.management_tailscale_ipv4")
    resolved = resolve_host_var_template(target, host_vars, "dns target")
    if resolved is target:
        return target
    return require_string(resolved, "dns target")


def resolve_host_var_template(value: Any, host_vars: dict[str, Any], path: str) -> Any:
    if not isinstance(value, str):
        return value
    match = HOST_VAR_TEMPLATE_RE.fullmatch(value)
    if match is None:
        return value
    return lookup_host_var_path(host_vars, match.group(1), path)


def lookup_host_var_path(host_vars: dict[str, Any], dotted_path: str, path: str) -> Any:
    keys = dotted_path.split(".")
    current: Any = host_vars
    traversed = []
    for key in keys:
        traversed.append(key)
        if not isinstance(current, dict) or key not in current:
            raise ValueError(f"{path} references unknown host var {'.'.join(traversed)!r}")
        current = current[key]
    return current


def render_known_templates_in_string(
    value: str,
    host_vars: dict[str, Any],
    guest_ipv4_by_name: dict[str, str],
    ports: dict[str, int],
    path: str,
) -> str:
    def replace_host_self(match: re.Match[str]) -> str:
        resolved = lookup_host_var_path(host_vars, match.group(1), path)
        return str(resolved)

    def replace_port_assignment(match: re.Match[str]) -> str:
        port_key = match.group(1)
        if port_key not in ports:
            raise ValueError(f"{path} references unknown port assignment {port_key!r}")
        return str(ports[port_key])

    def replace_guest_ipv4(match: re.Match[str]) -> str:
        guest_name = match.group(1)
        if guest_name not in guest_ipv4_by_name:
            raise ValueError(f"{path} references unknown proxmox guest {guest_name!r}")
        return guest_ipv4_by_name[guest_name]

    def replace_host_var(match: re.Match[str]) -> str:
        resolved = lookup_host_var_path(host_vars, match.group(1), path)
        return str(resolved)

    rendered = value
    previous = None
    while rendered != previous:
        previous = rendered
        rendered = PORT_ASSIGNMENT_TEMPLATE_RE.sub(replace_port_assignment, rendered)
        rendered = HOST_SELF_TEMPLATE_RE.sub(replace_host_self, rendered)
        rendered = GUEST_IPV4_TEMPLATE_RE.sub(replace_guest_ipv4, rendered)
        rendered = HOST_VAR_TEMPLATE_RE.sub(replace_host_var, rendered)
    return rendered


def render_known_templates(
    value: Any,
    host_vars: dict[str, Any],
    guest_ipv4_by_name: dict[str, str],
    ports: dict[str, int],
    path: str,
) -> Any:
    if isinstance(value, dict):
        return {
            key: render_known_templates(
                item,
                host_vars,
                guest_ipv4_by_name,
                ports,
                f"{path}.{key}",
            )
            for key, item in value.items()
        }
    if isinstance(value, list):
        return [
            render_known_templates(item, host_vars, guest_ipv4_by_name, ports, f"{path}[{index}]")
            for index, item in enumerate(value)
        ]
    if isinstance(value, str):
        return render_known_templates_in_string(value, host_vars, guest_ipv4_by_name, ports, path)
    return value


def extract_port(raw_url: str, path: str) -> int:
    match = re.search(r":(\d+)(?:/[^}]*)?$", raw_url)
    if match is None:
        raise ValueError(f"{path} must end with a port number")
    return int(match.group(1))


def service_url(scheme: str, host: str, port: int, suffix: str = "") -> str:
    return f"{scheme}://{host}:{port}{suffix}"


def load_sources() -> tuple[dict[str, Any], dict[str, Any]]:
    stack = require_mapping(load_yaml(STACK_PATH), str(STACK_PATH))
    host_vars = require_mapping(load_yaml(TOPOLOGY_HOST_VARS_PATH), str(TOPOLOGY_HOST_VARS_PATH))
    # Merge identity vars (platform_domain, operator email/name, etc.) into host_vars
    # so HOST_VAR_TEMPLATE_RE can resolve {{ platform_domain }} in strings like
    # "nginx.{{ platform_domain }}". Identity vars are loaded second so host_vars
    # wins on any key collision (operator-specific values take precedence).
    identity_vars = require_mapping(load_yaml(IDENTITY_VARS_PATH), str(IDENTITY_VARS_PATH))
    # Only merge plain string/int/bool values — skip Jinja2-templated values that
    # reference other identity vars (e.g. keycloak_oidc_issuer_url) to avoid
    # partially-rendered strings propagating into platform.yml.
    for key, val in identity_vars.items():
        if key not in host_vars and isinstance(val, (str, int, bool)) and "{{" not in str(val):
            host_vars[key] = val
    # Apply the local identity overlay (ADR 0407) — real deployment values override
    # the generic committed placeholders (e.g. example.com, 203.0.113.1).
    #
    # CRITICAL: This mirrors what _resolve_identity_override() does for Ansible runs
    # (injecting -e @.local/identity.yml as highest-precedence extra-vars).  Without
    # this step the generator would bake fake/sanitised IPs and domains into the
    # derived structures inside platform.yml (platform_host.management.ipv4, DNS
    # targets, monitoring scrape targets, …) that Ansible extra-vars cannot override
    # because they are nested mappings, not flat variables.
    #
    # Result: the private repo's platform.yml contains real deployment values; the
    # publish pipeline (make publish-serverclaw) sanitises them for the public mirror.
    local_identity_path = resolve_local_identity_override_path()
    if local_identity_path is not None:
        local_identity = require_mapping(load_yaml(local_identity_path), str(local_identity_path))
        for key, val in local_identity.items():
            # Local overlay WINS — override any committed placeholder with real value.
            # Skip Jinja2 templates (e.g. derived URLs that reference other vars).
            if isinstance(val, (str, int, bool)) and "{{" not in str(val):
                host_vars[key] = val
    return stack, host_vars


def load_optional_cross_cutting_generated_inputs() -> dict[str, Any]:
    """Load ADR 0374 generated artifacts when present.

    These files are intentionally ignored because they contain deployment-specific
    values. They still need to flow into platform.yml so playbooks that only load
    platform.yml can consume the generated hairpin/TLS/registry context.
    """

    def optional_yaml(path: Path) -> Any:
        if not path.exists():
            return None
        return load_yaml(path)

    def optional_mapping(path: Path, key: str) -> dict[str, Any]:
        payload = optional_yaml(path)
        if payload is None:
            return {}
        payload = require_mapping(payload, str(path))
        value = payload.get(key, {})
        return require_mapping(value, f"{path}.{key}") if value else {}

    def optional_list(path: Path, key: str) -> list[Any]:
        payload = optional_yaml(path)
        if payload is None:
            return []
        payload = require_mapping(payload, str(path))
        value = payload.get(key, [])
        return require_list(value, f"{path}.{key}") if value else []

    return {
        "dns_declarations": optional_mapping(CROSS_CUTTING_DNS_DECLARATIONS_PATH, "dns_records"),
        "nginx_upstreams": optional_list(CROSS_CUTTING_NGINX_UPSTREAMS_PATH, "platform_nginx_upstreams"),
        "sso_clients": optional_mapping(CROSS_CUTTING_SSO_CLIENTS_PATH, "sso_clients"),
        "hairpin_hosts": optional_list(CROSS_CUTTING_HAIRPIN_PATH, "platform_hairpin_nat_hosts"),
        "tls_certs": optional_mapping(CROSS_CUTTING_TLS_CERTS_PATH, "platform_tls_certs"),
    }


def build_guest_catalog(host_vars: dict[str, Any]) -> tuple[dict[str, Any], dict[str, Any], dict[str, str]]:
    guests = []
    by_name: dict[str, Any] = {}
    by_role: dict[str, Any] = {}
    ipv4_by_name: dict[str, str] = {}
    for index, guest in enumerate(require_list(host_vars.get("proxmox_guests"), "host_vars.proxmox_guests")):
        guest = require_mapping(guest, f"host_vars.proxmox_guests[{index}]")
        guest_copy = copy.deepcopy(guest)
        name = require_string(guest_copy.get("name"), f"host_vars.proxmox_guests[{index}].name")
        role = require_string(guest_copy.get("role"), f"host_vars.proxmox_guests[{index}].role")
        ipv4 = require_string(guest_copy.get("ipv4"), f"host_vars.proxmox_guests[{index}].ipv4")
        guests.append(guest_copy)
        by_name[name] = copy.deepcopy(guest_copy)
        by_role[role] = copy.deepcopy(guest_copy)
        ipv4_by_name[name] = ipv4
    return {"list": guests, "by_name": by_name, "by_role": by_role}, by_name, ipv4_by_name


def build_dns_records(
    service_topology: dict[str, Any],
    host_vars: dict[str, Any],
) -> dict[str, list[dict[str, Any]]]:
    def append_record(visibility: str, record: dict[str, Any]) -> None:
        bucket = records.setdefault(visibility, [])
        if record not in bucket:
            bucket.append(record)

    def managed_zone_name(service: dict[str, Any], path: str) -> str:
        dns = require_mapping(service.get("dns"), f"{path}.dns")
        public_hostname = require_string(service.get("public_hostname"), f"{path}.public_hostname")
        dns_name = require_string(dns.get("name"), f"{path}.dns.name")
        if dns_name == "@":
            return public_hostname
        prefix = f"{dns_name}."
        if not public_hostname.startswith(prefix):
            raise ValueError(
                f"{path}.public_hostname must start with {prefix!r} so wildcard aliases can derive the zone name"
            )
        zone_name = public_hostname[len(prefix) :]
        if not zone_name:
            raise ValueError(f"{path}.public_hostname must include a managed DNS zone suffix")
        return zone_name

    def relative_record_name(fqdn: str, zone_name: str, path: str) -> str:
        if fqdn == zone_name:
            return "@"
        suffix = f".{zone_name}"
        if not fqdn.endswith(suffix):
            raise ValueError(f"{path} must stay inside the managed DNS zone {zone_name}")
        return fqdn[: -len(suffix)]

    records = {
        "public": [
            {
                "name": "@",
                "type": "A",
                "value": require_string(host_vars.get("management_ipv4"), "host_vars.management_ipv4"),
                "ttl": 60,
            }
        ],
        "tailnet": [],
    }
    for service_id, service in service_topology.items():
        dns = service.get("dns")
        if not dns or not dns.get("managed"):
            continue
        visibility = dns["visibility"]
        append_record(
            visibility,
            {
                "name": dns["name"],
                "type": dns["type"],
                "value": dns["target"],
                "ttl": dns["ttl"],
            },
        )
        additional_records = require_list(
            dns.get("additional_records", []),
            f"service_topology.{service_id}.dns.additional_records",
        )
        for index, extra_record in enumerate(additional_records):
            extra_record = require_mapping(
                extra_record,
                f"service_topology.{service_id}.dns.additional_records[{index}]",
            )
            append_record(
                visibility,
                {
                    "name": require_string(
                        extra_record.get("name"),
                        f"service_topology.{service_id}.dns.additional_records[{index}].name",
                    ),
                    "type": require_string(
                        extra_record.get("type"),
                        f"service_topology.{service_id}.dns.additional_records[{index}].type",
                    ),
                    "value": resolve_dns_target(
                        require_string(
                            extra_record.get("target"),
                            f"service_topology.{service_id}.dns.additional_records[{index}].target",
                        ),
                        host_vars,
                    ),
                    "ttl": require_int(
                        extra_record.get("ttl"),
                        f"service_topology.{service_id}.dns.additional_records[{index}].ttl",
                    ),
                },
            )
        edge = require_mapping(service.get("edge", {}), f"service_topology.{service_id}.edge")
        aliases = require_string_list(edge.get("aliases", []), f"service_topology.{service_id}.edge.aliases")
        if visibility != "public" or not aliases:
            continue
        zone_name = managed_zone_name(service, f"service_topology.{service_id}")
        for index, alias in enumerate(aliases):
            append_record(
                visibility,
                {
                    "name": relative_record_name(
                        alias, zone_name, f"service_topology.{service_id}.edge.aliases[{index}]"
                    ),
                    "type": dns["type"],
                    "value": dns["target"],
                    "ttl": dns["ttl"],
                },
            )

    for index, record in enumerate(host_vars.get("mail_platform_dns_records", [])):
        record = require_mapping(record, f"host_vars.mail_platform_dns_records[{index}]")
        append_record(
            "public",
            {
                "name": require_string(record.get("name"), f"host_vars.mail_platform_dns_records[{index}].name"),
                "type": require_string(record.get("type"), f"host_vars.mail_platform_dns_records[{index}].type"),
                "value": resolve_dns_target(
                    require_string(
                        record.get("value"),
                        f"host_vars.mail_platform_dns_records[{index}].value",
                    ),
                    host_vars,
                ),
                "ttl": require_int(record.get("ttl"), f"host_vars.mail_platform_dns_records[{index}].ttl"),
            },
        )
    return records


def build_service_urls(
    service_id: str,
    service: dict[str, Any],
    host_vars: dict[str, Any],
    guest_ipv4_by_name: dict[str, str],
    ports: dict[str, int],
    stack: dict[str, Any],
) -> tuple[dict[str, Any], dict[str, str]]:
    host_id = require_string(stack["desired_state"]["host_id"], "versions/stack.yaml.desired_state.host_id")
    tailscale_ipv4 = require_string(host_vars.get("management_tailscale_ipv4"), "host_vars.management_tailscale_ipv4")
    owning_vm = service["owning_vm"]
    private_ip = tailscale_ipv4 if owning_vm == host_id else guest_ipv4_by_name[owning_vm]
    urls: dict[str, str] = {}
    port_map: dict[str, Any] = {}

    public_hostname = service.get("public_hostname")
    console_public_hostname = service.get("console_public_hostname")
    if public_hostname:
        urls["public"] = f"https://{public_hostname}"

    if service_id == "alertmanager":
        urls["internal"] = service_url("http", private_ip, ports["alertmanager_port"])
        port_map["internal"] = ports["alertmanager_port"]
    elif service_id == "grafana":
        urls["internal"] = service_url("http", private_ip, ports["monitoring_grafana_port"])
        urls["influxdb"] = service_url("http", private_ip, ports["monitoring_influxdb_port"])
        urls["loki_push"] = service_url("http", private_ip, ports["monitoring_loki_port"], "/loki/api/v1/push")
        urls["prometheus"] = service_url("http", private_ip, ports["monitoring_prometheus_port"])
        urls["tempo"] = service_url("http", private_ip, ports["monitoring_tempo_http_port"])
        urls["otlp_grpc"] = service_url("http", private_ip, ports["monitoring_otlp_grpc_port"])
