def build_service_topology(
    stack: dict[str, Any],
    host_vars: dict[str, Any],
    guest_ipv4_by_name: dict[str, str],
    ports: dict[str, int],
    service_classification_by_id: dict[str, dict[str, Any]],
) -> dict[str, Any]:
    raw_topology = require_mapping(host_vars.get("lv3_service_topology"), "host_vars.lv3_service_topology")
    host_id = require_string(stack["desired_state"]["host_id"], "versions/stack.yaml.desired_state.host_id")
    tailscale_ipv4 = require_string(host_vars.get("management_tailscale_ipv4"), "host_vars.management_tailscale_ipv4")

    resolved: dict[str, Any] = {}
    for service_id, raw_service in raw_topology.items():
        raw_service = require_mapping(raw_service, f"host_vars.lv3_service_topology.{service_id}")
        service = copy.deepcopy(raw_service)
        owning_vm = require_string(service.get("owning_vm"), f"host_vars.lv3_service_topology.{service_id}.owning_vm")
        if "private_ip" in service:
            private_ip_candidate = require_string(
                service.get("private_ip"),
                f"host_vars.lv3_service_topology.{service_id}.private_ip",
            )
            private_ip_candidate = require_string(
                resolve_host_var_template(
                    private_ip_candidate,
                    host_vars,
                    f"host_vars.lv3_service_topology.{service_id}.private_ip",
                ),
                f"host_vars.lv3_service_topology.{service_id}.private_ip",
            )
            if "proxmox_guests" in private_ip_candidate:
                private_ip = tailscale_ipv4 if owning_vm == host_id else guest_ipv4_by_name[owning_vm]
            else:
                private_ip = private_ip_candidate
        else:
            private_ip = tailscale_ipv4 if owning_vm == host_id else guest_ipv4_by_name[owning_vm]
        service["service_id"] = service_id
        service["private_ip"] = private_ip

        service_catalog_entry = service_classification_by_id.get(service_id, {})
        for field in (
            "runtime_pool",
            "deployment_surface",
            "restart_domain",
            "api_contract_ref",
            "mobility_tier",
        ):
            if field in service_catalog_entry:
                service[field] = copy.deepcopy(service_catalog_entry[field])

        if "dns" in service:
            service["dns"]["target"] = resolve_dns_target(
                require_string(service["dns"]["target"], f"host_vars.lv3_service_topology.{service_id}.dns.target"),
                host_vars,
            )

        ports_map, urls = build_service_urls(
            service_id,
            service,
            host_vars,
            guest_ipv4_by_name,
            ports,
            stack,
        )
        service["ports"] = ports_map
        service["urls"] = urls

        if "edge" in service and service["edge"].get("kind") == "proxy" and "upstream" not in service["edge"]:
            service["edge"]["upstream"] = urls["internal"]
        if "edge" in service and "action_url" in service["edge"] and service_id == "proxmox_ui":
            service["edge"]["action_url"] = urls["controller"]
        if "access" in service and "controller" in urls:
            service["access"]["url"] = urls["controller"]
        service = render_known_templates(
            service,
            host_vars,
            guest_ipv4_by_name,
            ports,
            f"host_vars.lv3_service_topology.{service_id}",
        )

        resolved[service_id] = service
    return resolved


def resolve_tcp_proxy_port(value: Any, ports: dict[str, int]) -> int:
    if isinstance(value, int):
        return value
    value = require_string(value, "proxy port template")
    match = re.fullmatch(r"{{\s*(?:platform_port_assignments\.)?([a-z0-9_]+)\s*}}", value)
    if match is None:
        raise ValueError(f"unsupported proxy port template: {value}")
    key = match.group(1)
    if key not in ports:
        raise ValueError(f"unknown proxy port key in template: {key}")
    return ports[key]


def build_tcp_proxies(host_vars: dict[str, Any], ports: dict[str, int]) -> list[dict[str, Any]]:
    resolved = []
    for index, proxy in enumerate(
        require_list(host_vars.get("proxmox_tailscale_tcp_proxies"), "host_vars.proxmox_tailscale_tcp_proxies")
    ):
        proxy = require_mapping(proxy, f"host_vars.proxmox_tailscale_tcp_proxies[{index}]")
        resolved.append(
            {
                "name": require_string(proxy.get("name"), f"host_vars.proxmox_tailscale_tcp_proxies[{index}].name"),
                "listen_address": require_string(
                    resolve_host_var_template(
                        proxy.get("listen_address"),
                        host_vars,
                        f"host_vars.proxmox_tailscale_tcp_proxies[{index}].listen_address",
                    ),
                    f"host_vars.proxmox_tailscale_tcp_proxies[{index}].listen_address",
                ),
                "listen_port": resolve_tcp_proxy_port(proxy.get("listen_port"), ports),
                "upstream_host": require_string(
                    proxy.get("upstream_host"),
                    f"host_vars.proxmox_tailscale_tcp_proxies[{index}].upstream_host",
                ),
                "upstream_port": resolve_tcp_proxy_port(proxy.get("upstream_port"), ports),
            }
        )
    return resolved


