def render_platform_vars(payload: dict[str, Any]) -> str:
    return GENERATED_HEADER + yaml_dump(payload)


def write_platform_vars(output_path: Path = PLATFORM_VARS_PATH) -> int:
    rendered = render_platform_vars(build_platform_vars())
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(rendered)
    print(f"Wrote generated platform vars to {output_path}")
    return 0


def check_platform_vars(output_path: Path = PLATFORM_VARS_PATH) -> int:
    rendered = render_platform_vars(build_platform_vars())
    current = output_path.read_text() if output_path.exists() else ""
    if rendered != current:
        print(
            "Generated platform vars are stale. Run "
            "'uvx --from pyyaml python scripts/generate_platform_vars.py --write' "
            "or 'make generate-platform-vars'.",
            file=sys.stderr,
        )
        return 2
    print("Generated platform vars OK")
    return 0


def dry_run(output_path: Path = PLATFORM_VARS_PATH) -> int:
    del output_path
    sys.stdout.write(render_platform_vars(build_platform_vars()))
    return 0


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate the committed platform facts library from canonical stack and host vars."
    )
    parser.add_argument("--write", action="store_true", help="Write inventory/group_vars/platform.yml.")
    parser.add_argument("--check", action="store_true", help="Exit non-zero if platform.yml is stale.")
    parser.add_argument("--dry-run", action="store_true", help="Print the generated YAML and exit.")
    args = parser.parse_args()

    selected = [args.write, args.check, args.dry_run]
    if sum(1 for enabled in selected if enabled) != 1:
        parser.print_help()
        return 0

    try:
        if args.write:
            return write_platform_vars()
        if args.check:
            return check_platform_vars()
        return dry_run()
    except (OSError, RuntimeError, ValueError) as exc:
        return emit_cli_error("Platform vars generator", exc)


if __name__ == "__main__":
    sys.exit(main())
