#!/usr/bin/env python3
"""Emergency credential rotation after public repository exposure.

ADR 0405 — Regenerate all platform-generated credentials so that values
recoverable from public git history become useless.

Usage:
    # List all secrets that will be rotated (dry run):
    python scripts/emergency_credential_rotation.py --list

    # Preview what will be regenerated for a specific tier:
    python scripts/emergency_credential_rotation.py --tier t1 --plan

    # Rotate a specific tier:
    python scripts/emergency_credential_rotation.py --tier t1 --apply

    # Rotate everything:
    python scripts/emergency_credential_rotation.py --tier all --apply

    # Rotate a single secret by ID:
    python scripts/emergency_credential_rotation.py --secret keycloak_grafana_client_secret --apply
"""

